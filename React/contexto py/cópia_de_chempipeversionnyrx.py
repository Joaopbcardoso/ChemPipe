def fetch_kegg_data(
    compound_name: str
) -> Dict:
    """
    Busca pathways reais no KEGG
    """

    print("Consultando KEGG")

    try:
        search = safe_request(
            f"http://rest.kegg.jp/find/compound/{compound_name}"
        )

        if not search or not search.text:
            return {
                "pathways": []
            }

        first = (
            search.text
            .split("\n")[0]
            .split("\t")[0]
        )

        detail = safe_request(
            f"http://rest.kegg.jp/get/{first}"
        )

        if not detail:
            return {
                "pathways": []
            }

        pathways = []

        for line in detail.text.split("\n"):
            if line.startswith("PATHWAY"):
                value = (
                    line.replace(
                        "PATHWAY",
                        ""
                    ).strip()
                )
                pathways.append(value)

        return {
            "pathways": pathways
        }

    except:
        return {
            "pathways": []
        }


def estimate_metabolism(
    pathways: List[str]
) -> str:
    """
    Inferência metabólica real
    """

    if not pathways:
        return "Metabolismo geral"

    p = [
        x.lower()
        for x in pathways
    ]

    if any(
        "glycolysis" in x
        or "carbohydrate" in x
        or "glucose" in x
        for x in p
    ):
        return "Carboidratos"

    if any(
        "amino" in x
        for x in p
    ):
        return "Aminoácidos"

    if any(
        "lipid" in x
        or "fatty acid" in x
        for x in p
    ):
        return "Lipídios"

    if any(
        "xenobiotic" in x
        for x in p
    ):
        return "Xenobiótico"

    return "Metabolismo geral"


