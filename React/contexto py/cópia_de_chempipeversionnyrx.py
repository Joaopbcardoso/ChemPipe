def fetch_chebi_data(
    inchikey: Optional[str]
) -> Dict:
    """
    Busca:

    - CHEBI ID
    - ontology relations
    - concept
    - relation
    - parent concept
    - child concept
    - roles
    """

    print("Consultando ChEBI")

    empty = {
        "chebi_id": None,
        "concept": [],
        "relation": [],
        "parent_concept": [],
        "child_concept": [],
        "roles": []
    }

    chebi_id = search_chebi_id_via_unichem(
        inchikey
    )

    if not chebi_id:
        return empty

    numeric_id = chebi_id.split(":")[-1]

    url = (
        "https://www.ebi.ac.uk/chebi/backend/api/public/"
        f"compound/CHEBI%3A{numeric_id}/"
    )

    response = safe_request(url)

    if not response:
        empty["chebi_id"] = chebi_id
        return empty

    try:
        data = response.json()

        outgoing = (
            data.get(
                "ontology_relations",
                {}
            ).get(
                "outgoing_relations",
                []
            )
        )

        concept = []
        relation = []
        parent_concept = []
        child_concept = []

        for rel in outgoing:
            relation_type = rel.get(
                "relation_type",
                ""
            )

            target = rel.get(
                "final_name",
                ""
            )

            if not target:
                continue

            concept.append(target)
            relation.append(relation_type)

            if relation_type == "is a":
                parent_concept.append(target)

            if relation_type == "has part":
                child_concept.append(target)

        roles_raw = data.get(
            "roles_classification",
            []
        )

        roles = [
            r["name"]
            for r in roles_raw
            if r.get("name")
        ]

        return {
            "chebi_id": chebi_id,
            "concept": concept,
            "relation": relation,
            "parent_concept": parent_concept,
            "child_concept": child_concept,
            "roles": roles
        }

    except:
        empty["chebi_id"] = chebi_id
        return empty


