def search_chebi_id_via_unichem(
    inchikey: Optional[str]
) -> Optional[str]:
    """
    Usa UniChem para mapear:

    InChIKey → CHEBI ID

    src_id=7 → ChEBI
    """

    if not inchikey:
        return None

    print("Consultando UniChem")

    url = (
        f"https://www.ebi.ac.uk/unichem/rest/inchikey/"
        f"{inchikey}"
    )

    response = safe_request(url)

    if not response:
        return None

    try:
        data = response.json()

        for item in data:
            if str(
                item.get("src_id", "")
            ) == "7":

                chebi_raw = item.get(
                    "src_compound_id",
                    ""
                )

                if chebi_raw:
                    if not chebi_raw.upper().startswith(
                        "CHEBI:"
                    ):
                        chebi_raw = (
                            f"CHEBI:{chebi_raw}"
                        )

                    return chebi_raw

    except:
        return None

    return None


