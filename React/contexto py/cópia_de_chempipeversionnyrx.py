def estimate_ionization(
    smiles: Optional[str],
    xlogp: Optional[float],
    tpsa: Optional[float],
    charge: int
) -> str:
    """
    Determina modo de ionização mais provável
    para LC-MS / metabolômica.

    Estratégia:
    - grupos funcionais
    - charge
    - lipofilicidade
    - polaridade
    """

    (
        has_acidic,
        has_basic,
        has_phosphate,
        has_sulfonate
    ) = detect_functional_groups(
        smiles
    )

    # negativo
    if (
        charge < 0
        or has_phosphate
        or has_sulfonate
        or (
            has_acidic
            and not has_basic
        )
    ):
        return "ESI negativo"

    # positivo
    if (
        charge > 0
        or (
            has_basic
            and not has_acidic
        )
    ):
        return "ESI positivo"

    # anfotérico
    if (
        has_basic
        and has_acidic
    ):
        return (
            "ESI positivo/negativo "
            "(anfotérico)"
        )

    # muito apolar
    if (
        xlogp is not None
        and xlogp > 3
        and tpsa is not None
        and tpsa < 40
    ):
        return (
            "APCI "
            "(composto apolar)"
        )

    return "ESI positivo (padrão)"

