def detect_functional_groups(
    smiles: Optional[str]
) -> Tuple[
    bool,
    bool,
    bool,
    bool
]:
    """
    Detecta grupos funcionais ionizáveis.

    Returns:
        (
            has_acidic,
            has_basic,
            has_phosphate,
            has_sulfonate
        )
    """

    if not smiles:
        return (
            False,
            False,
            False,
            False
        )

    # Remove estereoquímica
    s = (
        smiles
        .replace("@", "")
        .replace("/", "")
        .replace("\\", "")
    )

    # Ácidos
    has_acidic = bool(
        re.search(
            r"C\(=O\)O(?!C)",
            s
        )
        or re.search(
            r"S\(=O\)\(=O\)O",
            s
        )
    )

    # Fosfato
    has_phosphate = bool(
        re.search(
            r"P\(=O\)",
            s
        )
    )

    # Sulfonato
    has_sulfonate = bool(
        re.search(
            r"S\(=O\)\(=O\)\[O-\]",
            s
        )
    )

    # Bases
    has_basic = bool(
        re.search(
            r"N",
            s
        )
        and not re.search(
            r"C\(=O\)N",
            s
        )
    )

    return (
        has_acidic,
        has_basic,
        has_phosphate,
        has_sulfonate
    )


