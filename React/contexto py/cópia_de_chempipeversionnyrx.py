def main() -> None:
    """
    Execução principal.

    Permite:

    glucose, lactate, cholesterol

    separados por vírgula.
    """

    raw_input = input(
        "\nDigite compostos separados por vírgula:\n"
    ).strip()

    if not raw_input:
        print("Entrada inválida")
        return

    compounds = [
        x.strip()
        for x in raw_input.split(",")
        if x.strip()
    ]

    all_results = []

    for compound in compounds:
        result = integrate_compound_data(
            compound_name=compound
        )

        if result:
            all_results.append(result)

    if not all_results:
        print(
            "Nenhum resultado encontrado"
        )
        return

    print(
        "\n=============================="
        "\nRESULTADOS FINAIS"
        "\n==============================\n"
    )

    for item in all_results:
        print(
            json.dumps(
                item,
                indent=2,
                ensure_ascii=False
            )
        )
        print()

    export_results(
        all_results
    )


if __name__ == "__main__":
    main()

