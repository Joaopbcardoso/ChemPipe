def export_results(
    results: List[Dict]
) -> None:
    """
    Exporta:

    - CSV
    - XLSX

    formato pronto para:
    - artigo
    - TCC
    - submissão científica
    """

    if not results:
        print("Nenhum dado para exportar")
        return

    df = pd.DataFrame(results)

    csv_name = "metabolomics_results.csv"
    xlsx_name = "metabolomics_results.xlsx"

    df.to_csv(
        csv_name,
        index=False,
        encoding="utf-8-sig"
    )

    df.to_excel(
        xlsx_name,
        index=False
    )

    print(
        "\nArquivos exportados com sucesso:"
    )
    print(f"- {csv_name}")
    print(f"- {xlsx_name}")


