import re
from typing import Optional, List, Dict, Tuple

def count_oxygen_atoms(smiles: Optional[str]) -> int:
    return smiles.count('O') if smiles else 0

def count_nitrogen_atoms(smiles: Optional[str]) -> int:
    return smiles.count('N') if smiles else 0

def has_multiple_hydroxyls(smiles: Optional[str]) -> bool:
    return count_oxygen_atoms(smiles) >= 4 if smiles else False

def has_peptide_bonds(smiles: Optional[str]) -> bool:
    if not smiles: return False
    return len(re.findall(r'C\(=O\)N|NC\(=O\)', smiles)) >= 2

def has_steroid_core(smiles: Optional[str]) -> bool:
    if not smiles: return False
    return (smiles.count('1') + smiles.count('2')) >= 6 and count_oxygen_atoms(smiles) <= 3

def has_long_lipid_chain(smiles: Optional[str]) -> bool:
    return smiles.count('C') >= 14 if smiles else False

def has_aromatic_polyphenol(smiles: Optional[str]) -> bool:
    if not smiles: return False
    return 'c1' in smiles.lower() and count_oxygen_atoms(smiles) >= 3

def estimate_ionization(smiles: Optional[str], xlogp: Optional[float], tpsa: Optional[float], charge: int) -> str:
    if has_multiple_hydroxyls(smiles): return 'ESI positivo/negativo (adduct-based)'
    if has_peptide_bonds(smiles): return 'ESI positivo'
    if charge < 0: return 'ESI negativo'
    if charge > 0: return 'ESI positivo'
    if has_long_lipid_chain(smiles) and (xlogp or 0) > 4: return 'APCI (lipídico)'
    return 'ESI positivo'

def estimate_metabolism(pathways: List[str], smiles: Optional[str] = None) -> str:
    p = [x.lower() for x in pathways]
    if any(k in ' '.join(p) for k in ['glycolysis', 'glucose', 'carbohydrate']) or has_multiple_hydroxyls(smiles): return 'Carbohydrate metabolism'
    if any(k in ' '.join(p) for k in ['amino', 'protein']) or has_peptide_bonds(smiles): return 'Amino acid metabolism'
    if any(k in ' '.join(p) for k in ['lipid', 'fatty acid', 'steroid']) or has_long_lipid_chain(smiles) or has_steroid_core(smiles): return 'Lipid metabolism'
    return 'General metabolism'

