@lru_cache(maxsize=256)
def fetch_pubchem_data(
    compound_name: str
) -> Optional[Dict]:
    """
    Busca dados estruturais e físico-químicos
    do composto no PubChem.

    Retorna:
    - CID
    - fórmula
    - massa molecular
    - IUPAC
    - SMILES
    - InChIKey
    - XLogP
    - TPSA
    - Charge
    """

    print("Consultando PubChem")

    # ------------------------------
    # Busca CID
    # ------------------------------

    cid_url = (
        "https://pubchem.ncbi.nlm.nih.gov/rest/pug/"
        f"compound/name/{compound_name}/cids/JSON"
    )

    response = safe_request(cid_url)

    if not response:
        return None

    try:
        cid = response.json()[
            "IdentifierList"
        ]["CID"][0]

    except (
        KeyError,
        IndexError,
        ValueError
    ):
        return None

    # ------------------------------
    # Busca propriedades
    # ------------------------------

    properties_url = (
        "https://pubchem.ncbi.nlm.nih.gov/rest/pug/"
        f"compound/cid/{cid}/property/"
        "MolecularFormula,"
        "MolecularWeight,"
        "IUPACName,"
        "CanonicalSMILES,"
        "ConnectivitySMILES,"
        "InChIKey,"
        "XLogP,"
        "TPSA,"
        "Charge/JSON"
    )

    response = safe_request(
        properties_url
    )

    if not response:
        return None

    try:
        properties = response.json()[
            "PropertyTable"
        ]["Properties"][0]

        smiles = (
            properties.get("CanonicalSMILES")
            or properties.get(
                "ConnectivitySMILES"
            )
        )

        return {
            "cid": cid,
            "formula": properties.get(
                "MolecularFormula"
            ),
            "molecular_weight": properties.get(
                "MolecularWeight"
            ),
            "iupac_name": properties.get(
                "IUPACName"
            ),
            "smiles": smiles,
            "inchikey": properties.get(
                "InChIKey"
            ),
            "xlogp": properties.get(
                "XLogP"
            ),
            "tpsa": properties.get(
                "TPSA"
            ),
            "charge": properties.get(
                "Charge",
                0
            )
        }

    except (
        KeyError,
        IndexError,
        ValueError
    ):
        return None


