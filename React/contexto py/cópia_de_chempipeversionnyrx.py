def normalize_name(
    compound_name: str
) -> List[str]:
    """
    Gera variações prováveis do nome do composto
    para melhorar a taxa de acerto no PubChem.

    Exemplo:
    "D Glucose"

    →
    [
        "D Glucose",
        "d glucose",
        "DGlucose",
        "D-Glucose",
        "D_Glucose"
    ]
    """

    compound_name = compound_name.strip()

    variations = [
        compound_name,
        compound_name.lower(),
        compound_name.replace(" ", ""),
        compound_name.replace(" ", "-"),
        compound_name.replace("_", "-"),
        compound_name.replace("-", " "),
    ]

    # Remove duplicados preservando ordem
    unique = list(
        dict.fromkeys(variations)
    )

    return unique


def find_valid_compound_name(
    compound_name: str
) -> Optional[str]:
    """
    Descobre qual nome realmente existe
    no PubChem.

    Returns:
        Nome válido ou None
    """

    print("Validando nome no PubChem")

    for variant in normalize_name(compound_name):

        url = (
            "https://pubchem.ncbi.nlm.nih.gov/rest/pug/"
            f"compound/name/{variant}/cids/JSON"
        )

        response = safe_request(url)

        if response:
            return variant

    return None


